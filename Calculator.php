<?php

class Calculator {

    public function add($val1, $val2) {
        return $val1 + $val2;
    }

    public function subtract($val1, $val2) {
        return $val1 - $val2;
    }

    public function multiply($val1, $val2) {
        return $val1 * $val2;
    }

    public function divide($val1, $val2) {
        return $val1 / $val2;
    }

}

?>